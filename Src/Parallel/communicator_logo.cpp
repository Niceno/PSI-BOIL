#include "communicator.h"

#include <cstdio>
#include <ctime>

/***************************************************************************//**
*  Prints the PSI-Boil logo on the screen. 
*******************************************************************************/
void Communicator::logo() {
  std::cout << "________________________________________________________________________________" << '\n';
  std::cout << "      ______     ______   _____        ______     _____    _____   _ "    << '\n';
  std::cout << "     (_____ \\   / _____) (_____)      (_____ \\   / ___ \\  (_____) | |" << '\n';
  std::cout << "      _____) ) ( (____      _    ___   _____) ) | |   | |    _    | |"    << '\n';
  std::cout << "     |  ____/   \\____ \\    | |  (___) |  ___ (  | |   | |   | |   | |"  << '\n';
  std::cout << "     | |        _____) )  _| |_       | |___) ) | |___| |  _| |_  | |_____" << '\n';
  std::cout << "     |_|       (______/  (_____)      |______/   \\_____/  (_____) |_______)" << '\n';
  std::cout << "________________________________________________________________________________" << '\n';

  /* print current local time and date */
  time_t      rawtime;
  struct tm * timeinfo;
  time ( &rawtime );
  timeinfo = localtime ( &rawtime );
  std::cout << "                             " << asctime(timeinfo) << '\n';
}

